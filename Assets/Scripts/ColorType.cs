/// <summary>
/// Denotes which type of color your want from <see cref="ColorStore"/>
/// </summary>
public enum ColorType
{
    /// <summary>
    /// Color of the general HUD elements.
    /// </summary>
    HUDCOLOR,

    /// <summary>
    /// Color of dialogue from the narrator color.
    /// </summary>
    NARRATORCOLOR,

    /// <summary>
    /// Color of the player input.
    /// </summary>
    INPUTCOLOR,

    /// <summary>
    /// Color of descriptive and story dialogue.
    /// </summary>
    STORYCOLOR
}